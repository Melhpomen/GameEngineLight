game.Animation = function(array, ordre) // To animate every object of an array, you can specify an order
{
	var length = array.length;
	for ( var i = 0; i < length; i++ )
	{
		if ( array[i].active == null || array[i].active )
		{
			var callback = array[i].Animate(ordre);
			if ( callback == "destroy" ) { array.splice(i, 1); i--; length--; }
		}	
	}
}